public class Loops {
  public static void main(String[] args) {
    // if you want to repeatr things then loops may be a good option.
    // loops (to repeat statements)- while, do while, for

    // :: While ::
    int i = 1;

    while (i <= 4) {
      System.out.println("hi" + i);
      i++;
    }
  }
}

